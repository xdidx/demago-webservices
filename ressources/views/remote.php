<h3>
    Remote Gaming
</h3>

<div class="row">
    <div class="col s12">
        <div class="card-panel">
            <strong>Remote Gaming</strong> est un projet web permettant de jouer via son <strong>navigateur</strong> à des <strong>jeux non installés</strong> sur l'ordinateur client.
            <br/>
            Les entrées du client sont récupérées dans le navigateur en Javascript et sont envoyés vers un serveur NodeJS.
            <br/>
            Le serveur NodeJS se charge de simuler sur le serveur les touches envoyées par le client et envoie en retour l'ecran du jeu via le protocole WebRTC.
            <br/>
            A terme, le projet permettrait de tester n'importe quel jeu à distance, dont GTA V avec le mod installé ;)
        </div>
    </div>

