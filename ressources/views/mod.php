                    Informations
                </div>
                Grâce à un outil nommé ScriptHook, nous avons la possibilité de créer des librairies de code (DLL) à ajouter dans le dossier du jeu et qui seront interprétées par ce dernier.
                <br/>
                Nous sommes ensuite libre de faire beaucoup de modifcations dans le jeu en fonction des actions de l'utilisateur.
                <br/>
                Le modding sur GTA V avec ScriptHook se fait en C++ mais le "framework" nommé ScriptHookDotNet nous permet de coder en C# avec des objets et des méthodes plus simples à comprendre bien que les developpements soient assez similiaires
            </div>
