<h3>
    Launcher d'installation
</h3>

<div class="row">
    <div class="col s12">
        <div class="card-panel">
            Le launcher GTA Démago permet une installation beaucoup plus simplifié du mod.
            <br/>
            Il detecte la version du mod actuellement installée et propose de télécharger la dernière version disponible sur notre serveur.
            <br/>
            Il permet de désactiver le mod et ainsi pouvoir profiter du jeu en ligne sans risque de bannissement.
            <br/>
            Plusieurs fonctionnalités verront le jour avec le temps comme les classements en ligne, les dernières émissions en ligne...etc
        </div>
